main PROC far
    mov ax , @data
    mov ds , ax

    call UART_init
    call welcome_window

    mainMenuBeforeConnection:
        mov ah,0
        mov al,03h
        int 10h
        call startMainMenu
        mov notificationOption, 1   ; Option1: Welcomes the player
        handleNotificationSection notificationOption
        call assignPiecesColor
        call exchangePlayersNames

    mainMenuAfterConnection:
        mov ah,0
        mov al,03h
        int 10h
        call startMainMenu
        mov notificationOption, 2   ; Option2: Print other player name
        handleNotificationSection notificationOption
    
    ;press the key
    mov ah,0
    int 16h
    ;conditions
    ;F1
    mov bl,3Bh
    cmp ah,bl
    jz setChattingMode
    ;F2
    mov bl,3Ch
    cmp ah,bl
    jz setGameMode
    ;ESC
    mov bl,01
    cmp ah,bl
    jz setEsc

    mov cl,escEnc
    jmp continueToModeCheck

    setChattingMode:
        mov cl,chatModeEnc
        jmp continueToModeCheck

    setGameMode:
        mov cl,gameModeEnc
        jmp continueToModeCheck

    setEsc:
        mov cl,escEnc
        jmp continueToModeCheck
    
    continueToModeCheck:
    cmp cl,chatModeEnc
    je chatMode

    cmp cl,gameModeEnc
    je startGame

    jmp exit

    ;call the drawing module to draw the grid and pieces
    startGame :
        mov receiveParameter,1
        call initializeGame
        call initializeEatenArray
        mov al,0
        mov exitFlag,al
        mov exitFlagFromOtherSide,al
        ;Open the graphics mode
        mov ah,0
        mov al,13h
        int 10h
        call Draw
        call printGameInfo
        call inititializeChat
     ;   call moveReceivedPiece

        infiniteLoop: 
            call moveArrow
            mov al,exitFlag
            cmp al,1d
            je waitPress
            jmp infiniteLoop

    waitPress:
        mov ah,0
        int 16h
        call mainMenuAfterConnection

    chatMode:
        call startChatMode

    ;finish the execution and halting the program
    exit: 
        mov ah , 4ch
        int 21h

main ENDP

End main